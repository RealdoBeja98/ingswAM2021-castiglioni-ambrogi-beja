package it.polimi.ingsw.Utilities;

/**
 * class of obtain value from long
 */
public class ObtainIntValueFromLong {

    /**
     * This method is to transform a Long in a int
     * @param n of type Long
     * @return (int)(n).longValue() the result; of type int
     */
    public static int f(Long n){
        return (int)(n).longValue();
    }

}
